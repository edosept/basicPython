x = 5
print("nilai x pertama = ", x)

# penjumlahan
x += 2  # x = x + 2
print("nilai x setelah penjumlahan = ", x)

# pengurangan
x -= 2  # x = x - 2
print("nilai x setelah pengurangan = ", x)

# perkalian
x *= 2  # x = x * 2
print("nilai x setelah perkalian = ", x)

# pembagian
x /= 2  # x = x / 2
print("nilai x setelah pembagian = ", x)

# modulus
x %= 2  # x = x % 2
print("nilai x setelah modulus = ", x)

# pembagian kebawah
x = 19
print("="*50)
print("nilai x sekarang diubah menjadi = ", x)
x //= 5  # x = x // 5
print("nilai x setelah pembagian kebawah = ", x)

# pemangkatan
x **= 2  # x = x ** 2
print("nilai x setelah pemangkatan = ", x)
